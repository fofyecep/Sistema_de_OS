    //metodo para excluir OS
    private void excluir_os() {
        int confirma = JOptionPane.showConfirmDialog(null, "Tem Certeza que desaja excluir essa OS?", "Atenção", JOptionPane.YES_NO_OPTION);
        if (confirma == JOptionPane.YES_NO_OPTION) {
            String sql = "delete from tbos where os=?";
            try {
                pst = conexao.prepareStatement(sql);
                pst.setString(1, txtOs.getText());
                int apagado = pst.executeUpdate();
                if (apagado > 0) {
                    txtOs.setText(null);
                    txtData.setText(null);
                    txtCliId.setText(null);
                    txtOsEquip.setText(null);
                    txtOsDef.setText(null);
                    txtOsServ.setText(null);
                    txtOsTec.setText(null);
                    txtOsValor.setText(null);
                    //habilitar os objetos
                    btnOsAdicionar.setEnabled(true);
                    txtCliPesquisar.setEnabled(true);
                    tblClientes.setVisible(true);
                    JOptionPane.showMessageDialog(null, "OS Excluida com sucesso");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }
//metodo para imprimir uma OS

    private void imprimir_os() {
        // imprimindo uma OS
        int confirma = JOptionPane.showConfirmDialog(null, "Confirma a impressão desta OS?", "Atenção", JOptionPane.YES_NO_OPTION);
        if (confirma == JOptionPane.YES_OPTION) {
            //imprimindo a OS com o framework JasperReports
            try {
                //usando a classe HashMap para criar um filtro atraves do JasperViewer
                HashMap filtro = new HashMap();
                filtro.put("os",Integer.parseInt(txtOs.getText()));
                //usando a classe JasperPrint para preparar a impressao do relatorio
                JasperPrint print = JasperFillManager.fillReport("C:\\Users\\Carlos\\Documents\\Carlos\\DEBIAN SERVER\\reports\\os.jasper", filtro, conexao);
                //a linha abaixo exibe o relatorio atraves da classe JasperViwer
                JasperViewer.viewReport(print, false);

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }

    }

